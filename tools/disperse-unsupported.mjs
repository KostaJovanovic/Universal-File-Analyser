/* Re-check the stats "Unsupported types" dogpile against the live catalog.
   ============================================================================
   WHY: /api/stats pools every extension whose `supported` flag is 0 into a
   single "(unsupported)" bucket (worker/index.js). The worker flips a row to
   supported the next time someone drops that type (the supported = MAX(...)
   upsert), so newly-added formats disperse out of the dogpile on their own - but
   only once re-dropped. Rows for types nobody has dropped since support landed
   stay stuck in the bucket. This script sweeps them in one go.

   HOW: reads the single source of truth (assets/js/core/formats.js) for every
   extension Analyser now recognises - the classification *_EXTS sets plus every
   catalog row's exts - normalises each the way the Worker stores them
   (lowercase, a-z0-9 only, <= 16 chars; see cleanExt in worker/index.js), and
   writes an idempotent UPDATE to worker/disperse-unsupported.sql that flips any
   matching ext_stats row to supported = 1. Re-running is safe.

   RUN:
     node tools/disperse-unsupported.mjs
   then apply it to the live D1 (only you can - it needs your wrangler auth):
     wrangler d1 execute analyser-stats --remote --file=worker/disperse-unsupported.sql
   Preview which rows it would move first (optional):
     wrangler d1 execute analyser-stats --remote \
       --command "SELECT ext, count FROM ext_stats WHERE supported = 0 ORDER BY count DESC"
   ============================================================================ */
import { writeFileSync } from 'node:fs';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = join(ROOT, 'worker', 'disperse-unsupported.sql');

const fmts = await import(pathToFileURL(join(ROOT, 'assets/js/core/formats.js')).href);

// Same normalisation the Worker applies before storing an extension key, so the
// IN-list matches the actual ext_stats primary keys. Anything that cleans to ''
// ('(none)') or to > 16 chars ('(other)') is dropped - those are the Worker's
// catch-all buckets, never a real recognised type.
function cleanExt(raw) {
  const e = String(raw || '').toLowerCase().replace(/[^a-z0-9]/g, '');
  return (!e || e.length > 16) ? null : e;
}

const supported = new Set();
const add = (tok) => { const c = cleanExt(tok); if (c) supported.add(c); };

// 1. Classification sets that route a drop to a real renderer (= "supported").
for (const key of ['PHOTO_EXTS', 'AUDIO_EXTS', 'VIDEO_EXTS', 'CSV_EXTS', 'SVG_EXTS',
  'HEIC_EXTS', 'RAW_EXTS', 'DOC_EXTS', 'ARCHIVE_EXTS']) {
  const set = fmts[key];
  if (set) for (const e of set) add(e);
}

// 2. Every extension token listed in the display catalog (full analysis + every
//    identification row). This is the surface the site advertises as supported.
for (const row of [...fmts.FULL_ANALYSIS, ...fmts.IDENTIFICATION]) {
  for (const tok of row.exts.split(/\s+/)) add(tok);
}

const exts = [...supported].sort();
const values = exts.map((e) => `'${e}'`).join(', ');

const sql = `-- Generated by tools/disperse-unsupported.mjs - do not hand-edit.
-- Flips every ext_stats row that is currently pooled as unsupported but is now a
-- recognised type to supported = 1, so its accumulated count leaves the
-- "(unsupported)" bucket and lists individually on /stats. Idempotent.
-- ${exts.length} recognised extensions as of generation.
UPDATE ext_stats SET supported = 1
WHERE supported = 0 AND ext IN (
  ${values}
);
`;

writeFileSync(OUT, sql);
console.log(`Wrote ${OUT}`);
console.log(`${exts.length} recognised extensions in the dispersal set.`);
console.log('\nApply to the live database with:');
console.log('  wrangler d1 execute analyser-stats --remote --file=worker/disperse-unsupported.sql');
